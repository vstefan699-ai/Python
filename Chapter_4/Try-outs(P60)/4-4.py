# Create a list containing numbers from 1 to 10
# list() explicitly converts the range sequence into a list object
numbers = list(range(1, 11))

# Use a 'for' loop to iterate through each item in the list
# 'number' takes on the value of the current item in the iteration
for number in numbers:
    # Print the value to the console
    print(number)