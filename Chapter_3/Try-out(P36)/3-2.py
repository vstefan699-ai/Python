names = ['Pete', 'Andy', 'Bob']

# We use an 'f' before the quotes to create an f-string.
# The curly braces { } tell Python to "plug in" the value from the list.
print(f"Hey {names[0]}, thanks for always being there")
print(f"Hey {names[1]}, thanks for always being there")
print(f"Hey {names[2]}, thanks for always being there")